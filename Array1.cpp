/*C++ program that gets the product of all numbers in an array and prints them out*/


#include <iostream>

using namespace std;

int main()
{
    int n;
    int numbers[6];
    cout << "Enter numbers in Array: "<<endl;
    while(n<6){
        cin >> numbers[n];
        ++n;
    }
    int i, product=1;
    for(i=0;i<n;++i){
        product *= numbers[i];

    }
    cout <<"Product of numbers in array is " <<product << endl;
    return 0;
}
